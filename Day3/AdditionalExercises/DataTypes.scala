object DataTypes {
	
	def main ( args: Array[String] ) = {

		val someInt: Int = 1
		val isTrue: Boolean = true
		val letter: Char = 's'
		val pi: Double = 3.14159265
		val piSinglePrecision: Float = 3.14159265f
		val bigNumber: Long = 123456789
		val smallNumber: Byte = 127

		println ( s"Value of someInt = $someInt" )
		println ( s"Value of isTrue = $isTrue" )
		println ( s"Value of letter = $letter" )
		println ( s"Value of pi = $pi" )
		println ( s"Value of piSinglePrecision = $piSinglePrecision" )
		println ( s"Value of bigNumber = $bigNumber" )
		println ( s"Value of smallNumber = $smallNumber" )

	}

}
